int main() {
    int num1, num2, result;

    // Taking two integers as input
    scanf("%d", &num1);

    scanf("%d", &num2);

    // Performing bitwise OR operation
    result = num1 | num2;

    // Printing the result
    printf("%d\n", num1, num2, result);

    return 0;
}
